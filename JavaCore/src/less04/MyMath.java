package less04;

public class MyMath {
	
	static final  double  FIRST = 10.2;
	
	static final double SECOND = 2.7;
	
	
	static void add(){
		System.out.println(FIRST+SECOND);
	}
	
	static void substract(){
		System.out.println(FIRST-SECOND);
	}
	
	static void multiply(){
		System.out.println(FIRST*SECOND);
	}
	
	static void divide(){
		System.out.println(FIRST/SECOND);
	}

}
	
